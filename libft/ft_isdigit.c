#include <unistd.h>

int		ft_isdigit(int i)
{
	if (i >= 48 && i <= 57)
		return (1);
	else
		return (0);
}

int		main()
{
	if	(ft_isdigit(5) == 1)
		write(1, "1", 1);
	else
		write(1, "0", 1);
	return (0);

}
